/*PRIMERA PARTE*/
CREATE TABLE rol
(
  id_rol numeric NOT NULL primary key,
  descripcion varchar(200) NOT NULL
);
CREATE TABLE usuario
(
  id_usuario varchar(8) primary key,
  usuario varchar(30) NOT NULL,
  contrasena varchar(30) NOT NULL,
  dir_imagen varchar(100),
  id_rol numeric NOT NULL
);
CREATE TABLE cargo
(
  id_cargo varchar(8) NOT NULL primary key,
  nombre_cargo varchar(200) NOT NULL,
  salario numeric
);
CREATE TABLE persona
(
  id_persona numeric NOT NULL primary key,
  numero_ci varchar(20) NOT NULL,
  nombre_persona varchar(50) NOT NULL,
  apellido_persona varchar(50),
  telefono numeric,
  correo varchar(50),
  fecha_nacimiento date
);
CREATE TABLE personal
(
  id_persona numeric NOT NULL primary key,
  fecha_contratacion date,
  id_cargo varchar(8),
  id_usuario varchar(8)
)
INHERITS (persona);



