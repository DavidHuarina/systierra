CREATE VIEW v_datos_usuario AS 
 SELECT u.id_rol, p.id_cargo, p.id_usuario, p.id_persona, p.numero_ci, 
    p.nombre_persona, p.apellido_persona, p.telefono, p.correo, 
    p.fecha_nacimiento, p.fecha_contratacion, u.usuario, u.contrasena, 
    u.dir_imagen, c.nombre_cargo, c.salario, r.descripcion
   FROM personal p
   JOIN usuario u USING (id_usuario)
   JOIN cargo c USING (id_cargo)
   JOIN rol r USING (id_rol)
  ORDER BY u.usuario;


